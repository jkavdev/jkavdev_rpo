CREATE TABLE CONTA_CORRENTE(
	ID INT NOT NULL AUTO_INCREMENT,
	NUMERO VARCHAR(12) NOT NULL,
	SALDO DECIMAL,
	CODIGO_CLIENTE BIGINT NOT NULL,
	PRIMARY KEY(ID),
	FOREIGN KEY(CODIGO_CLIENTE)
		REFERENCES CLIENTE(ID)
);

