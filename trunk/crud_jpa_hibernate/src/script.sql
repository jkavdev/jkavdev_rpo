CREATE TABLE IF NOT EXISTS CLIENTE(
	ID INT NOT NULL AUTO_INCREMENT,
	NOME VARCHAR(100) NOT NULL,
	IDADE INTEGER,
	SEXO VARCHAR(1) NOT NULL,
	PROFISSAO VARCHAR(30),
	PRIMARY KEY(ID)
);

